  function formatRepo(repo) {
    if (repo.loading) {
      return "Searching...";
    }
    markup = '<option value="' + repo.d_id + '">' + repo.p_name + ', ' + repo.r_name + ', ' + repo.d_name + '</option>';
    return markup;
  }

  function formatRepoSelection(repo) {
    if (repo.id != "") {
      $(repo.element).attr('data-custom-district', repo.d_id);
      $(repo.element).attr('data-custom-regency', repo.regency_id);
      $(repo.element).attr('data-custom-province', repo.province_id);
      value = repo.p_name + ', ' + repo.r_name + ', ' + repo.d_name;
      return value;
    } else {
      return "Write the name of the city or sub-district address ...";
    }

  }

